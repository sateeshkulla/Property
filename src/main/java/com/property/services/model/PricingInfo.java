public class PricingInfo {
    private int monthlyRent;
    private int advance;
    private int parkingFees;
    private int electricityBill;
    private int trashFees;
    private int waterBill;
    private int totalMonthlyRent;
    public int getMonthlyRent() {
        return monthlyRent;
    }
    public void setMonthlyRent(int monthlyRent) {
        this.monthlyRent = monthlyRent;
    }
    public int getAdvance() {
        return advance;
    }
    public void setAdvance(int advance) {
        this.advance = advance;
    }
    public int getParkingFees() {
        return parkingFees;
    }
    public void setParkingFees(int parkingFees) {
        this.parkingFees = parkingFees;
    }
    public int getElectricityBill() {
        return electricityBill;
    }
    public void setElectricityBill(int electricityBill) {
        this.electricityBill = electricityBill;
    }
    public int getTrashFees() {
        return trashFees;
    }
    public void setTrashFees(int trashFees) {
        this.trashFees = trashFees;
    }
    public int getWaterBill() {
        return waterBill;
    }
    public void setWaterBill(int waterBill) {
        this.waterBill = waterBill;
    }
    public int getTotalMonthlyRent() {
        return totalMonthlyRent;
    }
    public void setTotalMonthlyRent(int totalMonthlyRent) {
        this.totalMonthlyRent = totalMonthlyRent;
    }
    
    
    
    

}
